package common.message;

import lombok.Data;

@Data
public class RpcResponseMessage extends AbstractRpcMessage {
    /**
     * 返回值
     */
    private Object returnValue;
    /**
     * 异常值
     */
    private Exception exceptionValue;

    @Override
    public int getMessageType() {
        return RPC_MESSAGE_TYPE_RESPONSE;
    }
}
